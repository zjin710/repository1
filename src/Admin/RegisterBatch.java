    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        if (selectedFile != null) {
            String userType = (String) userRoleCmb.getSelectedItem();
            String existingFile;
            if (null == userType) {
                JOptionPane.showMessageDialog(this, "Role not found.");
            } else {
                switch (userType) {
                    case "Student" -> {
                        existingFile = "student.txt";
                        admin.registerByBatch(selectedFile.getAbsolutePath(), existingFile, "Student");
                    }
                    case "Lecturer" -> {
                        existingFile = "lecturer.txt";
                        admin.registerByBatch(selectedFile.getAbsolutePath(), existingFile, "Lecturer");
                    }
                    default -> {
                        return;
                    }
                }
            }
            previousFrame.setVisible(true);
            this.dispose();
        } else {
            fileLbl.setText("No file selected.");
        }
    }//GEN-LAST:event_registerBtnActionPerformed

