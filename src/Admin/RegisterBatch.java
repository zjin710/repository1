    private void uploadFileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadFileBtnActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Text Files", "txt");
        fileChooser.setFileFilter(filter);

        int returnValue = fileChooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            selectedFile = fileChooser.getSelectedFile();
            if (selectedFile.getName().endsWith(".txt")) {
                fileLbl.setText("File selected: " + selectedFile.getName());
            } else {
                fileLbl.setText("Invalid file type. Please select a .txt file.");
                selectedFile = null; // Reset the selected file
            }
        } else {
            fileLbl.setText("No file selected.");
        }
    }//GEN-LAST:event_uploadFileBtnActionPerformed

